import SwiftUI

// MARK: - Pet Sprite Library

/// Central registry for all pet sprites organized by species and evolution stage
struct PetSpriteLibrary {
    /// Get the sprite for a specific pet configuration
    static func sprite(for species: PetSpecies, stage: EvolutionStage) -> PixelSprite {
        switch species {
        case .plant:
            return PlantSprites.sprite(for: stage)
        case .cat:
            return CatSprites.sprite(for: stage)
        case .dog:
            return DogSprites.sprite(for: stage)
        case .wolf:
            return WolfSprites.sprite(for: stage)
        case .dragon:
            return DragonSprites.sprite(for: stage)
        }
    }

    /// Get the animation for a specific pet based on mood
    static func animation(for species: PetSpecies, stage: EvolutionStage, mood: PetMood) -> SpriteAnimation {
        let sprite = self.sprite(for: species, stage: stage)
        let pixelAmount: CGFloat = 2  // Amount of movement in pixels

        switch mood {
        case .ecstatic:
            return .bounce(sprite: sprite, amount: pixelAmount * 1.5, frameDuration: 0.15)
        case .happy:
            return .bob(sprite: sprite, amount: pixelAmount, frameDuration: 0.4)
        case .content:
            return .bob(sprite: sprite, amount: pixelAmount * 0.5, frameDuration: 0.8)
        case .sad:
            return .droop(sprite: sprite, amount: pixelAmount, frameDuration: 1.0)
        case .unhappy:
            return .shiver(sprite: sprite, amount: pixelAmount * 0.5, frameDuration: 0.15)
        case .miserable:
            return .droop(sprite: sprite, amount: pixelAmount * 1.5, frameDuration: 1.5)
        }
    }

    /// Get the display size multiplier for each evolution stage
    static func sizeMultiplier(for stage: EvolutionStage) -> CGFloat {
        switch stage {
        case .baby: return 1.0
        case .child: return 1.15
        case .teen: return 1.3
        case .adult: return 1.5
        }
    }

    /// Get base pixel size for different display contexts
    static func pixelSize(for context: SpriteContext, stage: EvolutionStage) -> CGFloat {
        let baseSize: CGFloat = {
            switch context {
            case .home: return 6.0  // Increased from 4.0 for larger pet display
            case .card: return 3.0
            case .detail: return 5.0
            case .icon: return 2.0
            case .celebration: return 6.0
            }
        }()
        return baseSize * sizeMultiplier(for: stage)
    }
}

// MARK: - Sprite Context

/// Different contexts where sprites are displayed
enum SpriteContext {
    case home        // Main home screen display
    case card        // In cards and lists
    case detail      // Detail view
    case icon        // Small icon size
    case celebration // Level up/evolution celebration
}

